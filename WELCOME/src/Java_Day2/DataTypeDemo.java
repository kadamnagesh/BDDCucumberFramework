package Java_Day2;

public class DataTypeDemo {

	public static void main(String[] args) {
		// Integer DataType
				byte b=1;
				short s=2;
				int i=10000000;
				long l=4;
			
				//Floating point
				
				float f=10.3f;
				double d=33.55d;
				
				// character : denoted in single quote
				
				char c='a';
				char v='1';
				char p='$';
				
				// boolean  : values  -> true/false
				
				boolean flag=true;
				
				// String

				String userName="Myname" ;
				String Password="abc@123";
				
				// to store the age of person
				
				int age=20;
				
				String q="10";
				String w="20";
				System.out.println(q+w);

	}

}
