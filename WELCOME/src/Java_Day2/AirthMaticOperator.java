package Java_Day2;

public class AirthMaticOperator {

	public static void main(String[] args) {
		int a=30;
		int b=5;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		
		System.out.println("------------------------------");
		System.out.println(a/b);
		System.out.println("------------------------------");
		System.out.println(a%b);

	}

}
