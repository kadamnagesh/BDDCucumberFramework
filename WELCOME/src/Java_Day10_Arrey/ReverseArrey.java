package Java_Day10_Arrey;

public class ReverseArrey {

	public static void main(String[] args) {
		
		//print array in reverse

		   int a [] = { 1, 2, 3, 7, 8, 9 };
	       
	       System.out.println("reverse the array is");
	       
	        for (int i=(a.length-1); i >= 0; i--)
	    	  {
	        	
	           System.out.print(a[i]+"  ");
	           
	         }
	}
	
}

